// TRANSLATION
// Если сообщение начинется со слова Чат, ...
export const commonPrompt = `
Сохраняй оригинальное форматирование текста.
- Если в исходном тексте есть заглавные буквы, оставляй их.
- Если в исходном тексте нет заглавных букв, не добавляй их.
- Если в конце предложения есть точка, оставляй её.
- Если в конце предложения нет точки, не добавляй её.
- Если есть пустые строки, сохраняй их.
- Если есть предложения состоящие только из вопросительного знака - сохраняй как есть.
`;
export const translationPrompts = (lang: string): string => {
    const langInfo = languages.find((l) => l.key === lang);
    if (!langInfo) throw new Error(`Язык "${lang}" не найден`);
    return `
Ты профессиональный переводчик с русского на ${langInfo.accusative} и с ${langInfo.genitive} на русский.
- Если текст на русском, переведи на ${langInfo.accusative}.
- Если текст на ${langInfo.prepositional}, переведи на русский.
${commonPrompt}
`.trim();
};


